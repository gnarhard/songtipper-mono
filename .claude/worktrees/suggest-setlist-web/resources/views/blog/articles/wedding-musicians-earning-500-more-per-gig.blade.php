    <p>
        Wedding musicians are in a unique position. The crowd is celebrating, emotions are high, and people are in a generous mood. But most wedding performers leave hundreds of dollars on the table every single event simply because they don't have a system in place to capture that energy.
    </p>

    <h2 class="mt-8 mb-4 text-xl font-semibold text-ink dark:text-ink-inverse">Why Weddings Are a Tipping Goldmine</h2>
    <p>
        Think about the typical wedding guest. They're dressed up, they've had a few drinks, and they're watching people they love celebrate. When their favorite song comes on, they light up. Now imagine they could pull out their phone, find that song in your repertoire, and request it for the next dance — with a tip attached. That impulse is powerful, and it happens dozens of times per reception.
    </p>

    <h2 class="mt-8 mb-4 text-xl font-semibold text-ink dark:text-ink-inverse">The Old Way vs. The New Way</h2>
    <p>
        In the old model, a wedding musician gets a flat fee and maybe a cash tip jar that collects $20-40 over the course of an evening. The new model adds a digital request layer on top of your performance. Guests browse your song list, submit requests, and attach tips — all from their phone. The musician keeps playing. The tips keep flowing.
    </p>
    <p>
        The difference in per-gig income is staggering. Musicians using digital request platforms at weddings report earning $500 or more in tips alone on top of their performance fee. Some see even higher numbers at larger events.
    </p>

    <h2 class="mt-8 mb-4 text-xl font-semibold text-ink dark:text-ink-inverse">How to Set It Up</h2>
    <ul class="mb-4 list-disc pl-6">
        <li><strong>Prepare your repertoire in advance</strong> — have a curated wedding-appropriate song list ready to go, searchable by title and artist</li>
        <li><strong>Coordinate with the couple</strong> — let them know you accept digital requests and they can share the link with guests</li>
        <li><strong>Display QR codes at tables</strong> — place small cards or tent stands at each table so guests can scan and browse your songs</li>
        <li><strong>Set a reasonable minimum tip</strong> — weddings are premium events, so don't undersell yourself</li>
    </ul>

    <h2 class="mt-8 mb-4 text-xl font-semibold text-ink dark:text-ink-inverse">It's Not What You Think</h2>
    <p>
        The biggest surprise for most wedding musicians isn't the tip amounts — it's the volume. At a bar gig, maybe 5-10% of the room engages with requests. At a wedding, that number can jump to 30% or higher. Every table becomes a potential source of requests and tips. The music is already great. The system just makes it profitable.
    </p>
