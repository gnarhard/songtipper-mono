    <p>
        When you're starting out, 30 songs feels like a lot. You rehearse them until they're tight, build a setlist, and hit the stage. But as gigs come in — different venues, different crowds, longer sets — 30 songs stops being enough fast. The question isn't whether to grow your repertoire. It's how to do it without burning out.
    </p>

    <h2 class="mt-8 mb-4 text-xl font-semibold text-ink dark:text-ink-inverse">The Scaling Trap</h2>
    <p>
        The naive approach to growing your setlist is brute force: learn more songs, practice more hours, keep everything in your head. This works until about song 60, at which point the maintenance burden starts eating into your practice time, your performance quality, and your sanity. You spend more time reviewing old material than learning new songs, and the whole process feels like a treadmill.
    </p>

    <h2 class="mt-8 mb-4 text-xl font-semibold text-ink dark:text-ink-inverse">Work Smarter, Not Harder</h2>
    <p>
        Performers who've scaled to 100, 200, or even 500+ songs didn't do it through raw willpower. They built systems. Here's what that looks like:
    </p>
    <ul class="mb-4 list-disc pl-6">
        <li><strong>Organized charts</strong> — every song has a chart that's accurate, annotated, and instantly accessible during performance. You don't need to memorize everything when your charts are right there.</li>
        <li><strong>Smart categorization</strong> — songs tagged by genre, energy level, key, and venue type so you can build appropriate setlists without scrolling through everything</li>
        <li><strong>Bulk import tools</strong> — when migrating from another system or adding a batch of new songs, doing it one by one is a non-starter. Bulk import lets you add 50 songs in the time it used to take to add 5.</li>
        <li><strong>AI-assisted metadata</strong> — let automation handle the tedious part of cataloging new songs so you can focus on actually learning the music</li>
    </ul>

    <h2 class="mt-8 mb-4 text-xl font-semibold text-ink dark:text-ink-inverse">The Rotation Strategy</h2>
    <p>
        Having a large repertoire doesn't mean playing every song at every gig. Smart performers use rotation: a core set of 30-40 songs that form the backbone of most performances, supplemented by venue-specific and request-driven selections from the broader library. This approach keeps your core tight while giving you the flexibility to adapt to any situation.
    </p>

    <h2 class="mt-8 mb-4 text-xl font-semibold text-ink dark:text-ink-inverse">Avoiding Burnout</h2>
    <p>
        The key to scaling without burnout is separating the <em>learning</em> from the <em>managing</em>. Learning new songs should be the fun part — the creative work that keeps you growing as a musician. Managing your repertoire, organizing charts, building setlists, and tracking what works should be handled by your tools, not your brain.
    </p>
    <p>
        When the administrative side of your repertoire is automated and organized, scaling feels natural instead of overwhelming. You add songs because you want to, not because you have to. And your setlists keep getting better because they're built on data and experience, not guesswork.
    </p>
</div>
