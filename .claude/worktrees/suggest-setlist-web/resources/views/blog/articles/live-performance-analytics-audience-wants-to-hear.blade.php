    <p>
        For decades, performers have relied on intuition to figure out what their audience wants. Read the room, adjust on the fly, hope for the best. But intuition has limits — and in 2026, the performers who are growing fastest are the ones who've added data to their toolkit.
    </p>

    <h2 class="mt-8 mb-4 text-xl font-semibold text-ink dark:text-ink-inverse">What Performance Analytics Actually Tell You</h2>
    <p>
        Live performance analytics go beyond basic setlist tracking. They show you patterns you'd never notice on your own: which songs get requested at which types of venues, what time of night generates the most engagement, which songs earn the highest tips on average, and how your repertoire performance changes over time.
    </p>

    <h2 class="mt-8 mb-4 text-xl font-semibold text-ink dark:text-ink-inverse">Your Most Valuable Metrics</h2>
    <ul class="mb-4 list-disc pl-6">
        <li><strong>Most Requested Songs</strong> — the songs your audience actively seeks out, which tells you what to keep in heavy rotation</li>
        <li><strong>Highest Earning Songs</strong> — the songs that generate the most tip revenue, not always the same as most requested</li>
        <li><strong>Request Frequency Over Time</strong> — trending songs that are gaining or losing popularity with your audience</li>
        <li><strong>Songs Played vs. Songs Requested</strong> — the gap between what you choose to play and what your audience wants to hear</li>
    </ul>

    <h2 class="mt-8 mb-4 text-xl font-semibold text-ink dark:text-ink-inverse">Audience Insights You're Missing</h2>
    <p>
        One of the most underrated benefits of performance analytics is understanding what your audience wants that you <em>don't</em> currently offer. When someone searches your repertoire for a song that isn't there, that's a signal. When a particular genre dominates your request queue, that's a signal. These insights help you strategically expand your repertoire in directions your audience is already pulling you.
    </p>

    <h2 class="mt-8 mb-4 text-xl font-semibold text-ink dark:text-ink-inverse">Cross-Performer Trends</h2>
    <p>
        Beyond your own data, seeing what other performers in your area or genre are getting requested can reveal opportunities. If a song is trending across the platform but isn't in your repertoire, that's a potential addition worth considering. You don't have to follow every trend, but awareness of what's working for others gives you an edge.
    </p>

    <h2 class="mt-8 mb-4 text-xl font-semibold text-ink dark:text-ink-inverse">From Data to Decisions</h2>
    <p>
        Analytics only matter if you act on them. Set aside 15 minutes after each gig to review your numbers. Which songs earned the most? Which got skipped? Are there requests you couldn't fill? Over time, these small reviews compound into a deeply optimized repertoire and setlist that's tuned to exactly what your audience wants to hear — and pay for.
    </p>
</div>
