    <p>
        Not all song requests are created equal. Some songs consistently earn higher tips, get requested more often, and create better crowd energy. If you want to maximize your earning potential as a live performer, understanding which songs pay is just as important as knowing how to play them.
    </p>

    <h2 class="mt-8 mb-4 text-xl font-semibold text-ink dark:text-ink-inverse">The High-Earning Song Profile</h2>
    <p>
        Songs that earn the most tips tend to share a few characteristics. They're emotionally resonant — think anthems, love songs, or crowd singalongs. They're well-known enough that multiple people in the room will recognize them, but not so overplayed that they feel stale. And they're songs that people associate with specific moments: weddings, celebrations, nostalgia.
    </p>

    <h2 class="mt-8 mb-4 text-xl font-semibold text-ink dark:text-ink-inverse">Track Your Data</h2>
    <p>
        The most reliable way to find your highest-earning songs is to track them. After a few weeks of accepting digital requests with tips, patterns emerge fast. You'll see which songs get requested repeatedly, which ones attract the highest average tip, and which ones barely get touched. This data is more valuable than any generic "top request" list because it's specific to <em>your</em> audience and <em>your</em> venues.
    </p>

    <h2 class="mt-8 mb-4 text-xl font-semibold text-ink dark:text-ink-inverse">Categories That Consistently Perform</h2>
    <ul class="mb-4 list-disc pl-6">
        <li><strong>Classic rock anthems</strong> — timeless crowd-pleasers that multiple generations enjoy</li>
        <li><strong>Current pop hits</strong> — especially songs trending on social media that younger audiences love</li>
        <li><strong>Wedding staples</strong> — first dance songs, father-daughter songs, and party starters</li>
        <li><strong>Regional favorites</strong> — songs that resonate specifically with your local audience</li>
        <li><strong>Deep cuts with passionate fanbases</strong> — fewer requests but significantly higher tips per request</li>
    </ul>

    <h2 class="mt-8 mb-4 text-xl font-semibold text-ink dark:text-ink-inverse">Build a High-Value Repertoire</h2>
    <p>
        Once you know which songs earn the most, adjust your repertoire accordingly. This doesn't mean only playing high-tip songs — variety matters. But it does mean making sure your strongest earners are well-rehearsed, prominently displayed in your song list, and ready to go at any gig.
    </p>
    <p>
        Look at what other performers in your area are getting requested too. Seeing what songs are trending across the platform can reveal opportunities you might be missing — songs your audience wants that you haven't added yet.
    </p>

    <h2 class="mt-8 mb-4 text-xl font-semibold text-ink dark:text-ink-inverse">The Compounding Effect</h2>
    <p>
        When you build a repertoire optimized for requests and tips, something interesting happens: your reputation grows. Audiences start coming specifically because they know they can request songs and you'll actually play them. Venues notice the higher engagement. And your per-gig income keeps climbing — not because you're working harder, but because you're working smarter.
    </p>
