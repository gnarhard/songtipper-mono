    <p>
        Every performer has songs they love playing — the ones that sound great, feel great, and get the crowd going. But if your audience doesn't know about them, those songs sit unplayed while you field the same tired requests. The trick isn't just having great material. It's guiding your audience toward it.
    </p>

    <h2 class="mt-8 mb-4 text-xl font-semibold text-ink dark:text-ink-inverse">Make Your Repertoire Browsable</h2>
    <p>
        When your audience can see your full song list, they discover songs they didn't know you played. Instead of defaulting to "Play Freebird," they browse, find something they actually love, and request it with enthusiasm — and a tip. A well-organized, searchable repertoire is your best marketing tool at every gig.
    </p>

    <h2 class="mt-8 mb-4 text-xl font-semibold text-ink dark:text-ink-inverse">The Psychology of Choice</h2>
    <p>
        When people browse a menu, they're more likely to order something they're excited about — and spend more doing it. Your song list works the same way. When someone discovers you play their favorite deep cut, they don't just request it. They tip generously because the experience feels personal. They feel seen.
    </p>

    <h2 class="mt-8 mb-4 text-xl font-semibold text-ink dark:text-ink-inverse">Tip-Ranked Queues Create Natural Incentives</h2>
    <p>
        Here's where it gets interesting. When your request queue ranks songs by tip amount, with earlier requests winning ties, you create a transparent system that audiences understand intuitively. Higher tip? Your song plays sooner. It's fair, it's simple, and it naturally encourages people to tip a little more to get their request bumped up.
    </p>
    <p>
        This isn't about being greedy. It's about creating a system where everyone wins: the audience gets their song, you get compensated for taking requests, and the queue stays manageable.
    </p>

    <h2 class="mt-8 mb-4 text-xl font-semibold text-ink dark:text-ink-inverse">Curate, Don't Just List</h2>
    <p>
        Not every song in your repertoire needs to be front and center. Think about which songs consistently get great reactions and which ones tend to fall flat. Your analytics can tell you which songs earn the most tips and get requested the most. Use that data to highlight your strongest material and retire the songs that aren't pulling their weight.
    </p>

    <h2 class="mt-8 mb-4 text-xl font-semibold text-ink dark:text-ink-inverse">The Bottom Line</h2>
    <p>
        Getting your audience to pay for your best songs isn't about being pushy. It's about removing barriers, presenting your music in a way that inspires engagement, and letting a smart queue system handle the rest. When requesting and tipping is effortless, people do it. And when they do, everyone has a better time.
    </p>
</div>
