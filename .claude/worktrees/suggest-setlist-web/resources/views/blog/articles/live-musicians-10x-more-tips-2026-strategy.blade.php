    <h2 class="mt-8 mb-4 text-xl font-semibold text-ink dark:text-ink-inverse">The Shift to Digital Requests</h2>
    <p>
        The biggest change isn't the music — it's the workflow. When your audience can browse your repertoire on their phone, pick a song, and attach a tip in under 30 seconds, they tip more often and they tip higher. Friction is the enemy of generosity, and removing it is the single highest-leverage move you can make.
    </p>

    <h2 class="mt-8 mb-4 text-xl font-semibold text-ink dark:text-ink-inverse">Why 10x Isn't Hyperbole</h2>
    <p>
        Think about a typical bar gig. Maybe you get a handful of cash tips — $20 to $40 on a good night. Now imagine every person in a 100-person room has your song list on their phone. Even if only 10% of them request a song with a $5 tip attached, that's $50 in a single set. Multiply that across a full night and the numbers add up fast.
    </p>
    <p>
        The performers seeing the biggest jumps aren't necessarily playing better — they're making it effortless for audiences to engage. A visible QR code, a well-organized repertoire, and a transparent queue system do the heavy lifting.
    </p>

    <h2 class="mt-8 mb-4 text-xl font-semibold text-ink dark:text-ink-inverse">The 2026 Strategy</h2>
    <p>
        Here's what top-earning performers are doing right now:
    </p>
    <ul class="mb-4 list-disc pl-6">
        <li>Displaying a QR code prominently at every gig so audience members can jump straight to the request page</li>
        <li>Curating a browsable repertoire rather than relying on shouted requests from across the room</li>
        <li>Setting a minimum tip amount that signals value without scaring anyone off</li>
        <li>Using a ranked queue where higher tips move songs up, creating a natural incentive to tip more</li>
        <li>Tracking which songs earn the most and doubling down on their strongest material</li>
    </ul>

