        You've played hundreds of gigs. Your setlist is solid. The crowd is having a great time. But when you count your tips at the end of the night, the number hasn't budged in months. What gives?
    </p>

    <h2 class="mt-8 mb-4 text-xl font-semibold text-ink dark:text-ink-inverse">It's Not the Music — It's the Workflow</h2>
    <p>
        Most performers who struggle with flat tip income assume it's about song selection, venue quality, or crowd size. And while those things matter, the hidden bottleneck is almost always <em>how</em> your audience interacts with you. If the only way to request a song is to walk up and shout over the music, most people simply won't bother. And if they can't tip without cash, you're leaving money on the table every single set.
    </p>

    <h2 class="mt-8 mb-4 text-xl font-semibold text-ink dark:text-ink-inverse">The Friction Problem</h2>
    <p>
        Here's the reality: your audience <em>wants</em> to engage. They want to hear their favorite song, and many of them are happy to tip for it. But every barrier you put between that impulse and the action kills conversions. No one carries cash anymore. Shouting requests is awkward. Writing on napkins gets lost.
    </p>
    <p>
        The performers who've solved this problem have done one simple thing: they've made requesting and tipping as easy as ordering a coffee on an app.
    </p>

    <h2 class="mt-8 mb-4 text-xl font-semibold text-ink dark:text-ink-inverse">The 24-Hour Fix</h2>
    <p>
        Here's what you can do today to start seeing results at your very next gig:
    </p>
    <ul class="mb-4 list-disc pl-6">
        <li><strong>Set up a digital request page</strong> — get your songs into a browsable list that your audience can access from their phone</li>
        <li><strong>Enable tipping on requests</strong> — let people attach a tip when they submit a song request, with suggested amounts that make it easy</li>
        <li><strong>Display a QR code</strong> — print it, put it on your stand, tape it to the tip jar. Make it impossible to miss</li>
        <li><strong>Announce it once per set</strong> — a quick mention that people can request songs from their phone is all it takes</li>
    </ul>

    <h2 class="mt-8 mb-4 text-xl font-semibold text-ink dark:text-ink-inverse">Why This Works Immediately</h2>
    <p>
        The moment you remove friction, you unlock demand that was always there. People who never would have walked up to make a request will happily tap their phone. And when tipping is built into the request flow — not an afterthought — the average tip per interaction goes up dramatically. You're not working harder. You're just making it easier for your audience to pay you what your performance is worth.
    </p>
