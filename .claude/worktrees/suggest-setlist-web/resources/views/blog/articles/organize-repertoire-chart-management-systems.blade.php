    <h2 class="mt-8 mb-4 text-xl font-semibold text-ink dark:text-ink-inverse">The Problem with Scattered Systems</h2>
    <p>
        Most performers start with a simple approach: a folder on their tablet, maybe a Google Drive link, a printed binder as backup. It works when you have 30 songs. But as your repertoire grows — 50, 100, 200+ songs — the cracks show fast. Files get duplicated. Naming conventions drift. You can't find the version of a chart you actually use. And updating your setlist for a specific gig means manually cross-referencing multiple sources.
    </p>

    <h2 class="mt-8 mb-4 text-xl font-semibold text-ink dark:text-ink-inverse">What a Good System Looks Like</h2>
    <p>
        A chart management system that actually works for gigging musicians needs a few things:
    </p>
    <ul class="mb-4 list-disc pl-6">
        <li><strong>One source of truth</strong> — every chart lives in one place, with one canonical version per song</li>
        <li><strong>Metadata that's actually useful</strong> — key, tempo, genre, artist, and any custom tags you use to organize your workflow</li>
        <li><strong>Fast search</strong> — you should be able to find any chart in seconds, even mid-performance</li>
        <li><strong>Setlist integration</strong> — charts should link directly to your setlists so you can build a set and have every chart ready to go</li>
        <li><strong>Annotation support</strong> — mark up charts with personal notes, key changes, or performance cues without altering the original</li>
    </ul>

    <h2 class="mt-8 mb-4 text-xl font-semibold text-ink dark:text-ink-inverse">The AI Shortcut</h2>
    <p>
        One of the biggest time sinks in repertoire management is data entry. Entering song title, artist, key, tempo, and genre for every song in your library is tedious — and it's exactly the kind of work that AI handles well. Modern chart management tools can read your uploaded PDF charts and automatically extract metadata, saving you hours of manual work.
    </p>
