<div class="space-y-4 text-ink-muted dark:text-ink-soft">
    <p>
        Every song you play at a gig is either earning you money or costing you an opportunity. If you're spending set time on songs nobody asked for and nobody tips on, you're actively leaving money on the table. Smart setlist management means knowing what works — and having the discipline to drop what doesn't.
    </p>

    <h2 class="mt-8 mb-4 text-xl font-semibold text-ink dark:text-ink-inverse">The Cost of a Bad Song Choice</h2>
    <p>
        It sounds dramatic, but think about it. You have a limited number of songs per set. Every slot occupied by a song that doesn't engage the audience is a slot that could have been filled by a requested, tipped song. Over the course of an evening, those missed opportunities compound. Five wrong songs across a four-hour gig could represent dozens of lost request opportunities and hundreds in unrealized tips.
    </p>

    <h2 class="mt-8 mb-4 text-xl font-semibold text-ink dark:text-ink-inverse">Stop Guessing, Start Tracking</h2>
    <p>
        The biggest mistake performers make with setlists is relying on gut feel instead of data. You might think a song is a crowd favorite because it went well at one gig three months ago. But without tracking request frequency, tip amounts, and audience engagement over time, you're flying blind.
    </p>
    <p>
        Performance analytics change the game. When you can see that Song A gets requested 5x more than Song B and earns 3x the tips, the decision about which one to keep in your setlist makes itself.
    </p>

    <h2 class="mt-8 mb-4 text-xl font-semibold text-ink dark:text-ink-inverse">The Setlist Management Framework</h2>
    <ul class="mb-4 list-disc pl-6">
        <li><strong>Audit your repertoire quarterly</strong> — review which songs are getting requested, which earn tips, and which sit untouched</li>
        <li><strong>Rotate low performers out</strong> — if a song hasn't been requested in 30 days, consider replacing it with something your data suggests will work better</li>
        <li><strong>Test new songs strategically</strong> — add one or two new songs per month and track their performance before committing them to permanent rotation</li>
        <li><strong>Build setlists from your winners</strong> — your pre-planned setlist should be your greatest hits, with gaps left for audience requests</li>
    </ul>

    <h2 class="mt-8 mb-4 text-xl font-semibold text-ink dark:text-ink-inverse">Tools That Actually Help</h2>
    <p>
        Managing all of this in a spreadsheet or in your head is a recipe for frustration. A dedicated setlist management tool that integrates with your request and tipping data saves time and gives you actionable insights. You need to see at a glance which songs are earning their spot and which ones need to go.
    </p>
    <p>
        The goal isn't to play it safe with the same 20 songs every night. It's to make informed decisions about what your audience actually wants — and deliver it consistently.
    </p>
