    <h2 class="mt-8 mb-4 text-xl font-semibold text-ink dark:text-ink-inverse">The Data Tells a Story</h2>
    <p>
        Across hundreds of performances, certain setlist structures consistently outperform others in generating audience requests and tips. It's not about having the longest setlist or the most obscure songs. It's about balance, pacing, and giving your audience just enough variety to keep them engaged — and motivated to participate.
    </p>

    <h2 class="mt-8 mb-4 text-xl font-semibold text-ink dark:text-ink-inverse">Start Strong, Leave Room</h2>
    <p>
        Performers who open with a well-known crowd-pleaser see significantly more requests in the first 30 minutes compared to those who start with originals or deep cuts. The logic is simple: an energetic opener signals to the audience that requests are welcome and worth making. Once people see the queue filling up, social proof kicks in and more follow.
    </p>

    <h2 class="mt-8 mb-4 text-xl font-semibold text-ink dark:text-ink-inverse">The Sweet Spot: 40-80 Songs</h2>
    <p>
        Performers with repertoires between 40 and 80 songs displayed in their browsable list generated the highest request rates. Too few songs and the audience feels limited. Too many and they get overwhelmed by choice and don't request at all. The ideal range gives enough variety to cover different tastes while keeping the list scannable.
    </p>

    <h2 class="mt-8 mb-4 text-xl font-semibold text-ink dark:text-ink-inverse">Mix Eras and Genres</h2>
    <p>
        Setlists that blend multiple decades and genres — classic rock alongside current pop, country mixed with R&B — see 35% more requests than single-genre setlists. Mixed audiences want to feel represented, and a diverse repertoire ensures more people in the room see something they want to hear.
    </p>

    <h2 class="mt-8 mb-4 text-xl font-semibold text-ink dark:text-ink-inverse">The Request Gap Strategy</h2>
    <p>
        The top-earning performers don't fill every slot in their setlist. They intentionally leave gaps — planned moments where they pull from the request queue instead of their pre-planned set. These gaps create a natural rhythm: play a few planned songs to maintain energy, then check the queue and play the highest-tipped request. This keeps the audience engaged because they see that requests are actually being played, which motivates more people to submit.
    </p>

    <h2 class="mt-8 mb-4 text-xl font-semibold text-ink dark:text-ink-inverse">Key Takeaways</h2>
    <ul class="mb-4 list-disc pl-6">
        <li>Open with a recognizable song to prime the audience for requests</li>
        <li>Keep your displayed repertoire between 40-80 songs</li>
        <li>Mix eras and genres to appeal to the full room</li>
        <li>Leave intentional gaps in your setlist to pull from the request queue</li>
        <li>Track which songs generate the most requests and tips, then build future setlists around your proven winners</li>
    </ul>
